namespace AmeliaBooking\Application\Commands\Bookable\Resource;

use AmeliaBooking\Application\Commands\Command;

/**
 * Class GetResourceCommand
 *
 * @package AmeliaBooking\Application\Commands\Bookable\Resource
 */
class GetResourceCommand extends Command
{
    /**
     * GetResourceCommand constructor.
     *
     * @param $args
     */
    public function __construct($args)
    {
        parent::__construct($args);
        if (isset($args['id'])) {
            $this->setField('id', $args['id']);
        }
    }
}
